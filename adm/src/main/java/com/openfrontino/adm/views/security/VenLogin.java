    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        if (this.txtLogin.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe Ingresar Nombre de Usuario", "Validacion de Datos", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        if (this.txtPassword.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Debe Ingresar Contrase침a", "Validacion de Datos", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        ConnectionTool conex = new ConnectionTool();
        if (!conex.connect(dtoSes.getDbhost(), dtoSes.getDbport(), dtoSes.getDbname(), dtoSes.getDbuser(),
                dtoSes.getDbpassword()
        )) {
            dtoSes = new SessionDto();
            JOptionPane.showMessageDialog(null, "Error Conectando con Base de Datos", "Configuraci칩n", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        Connection con = conex.getConexion();
        boolean commit = false;
        try {
            Session ses = new Session();
            ses.setCon(con);
            //Validar Usuario
            StringBuilder password = new StringBuilder();
            for (int i = 0; i < this.txtPassword.getPassword().length; i++) {
                password.append(this.txtPassword.getPassword()[i]);
            }
            dtoSes.setDtoUsu(ses.logearUsuario(this.txtLogin.getText(), password.toString()));
            if (dtoSes.getDtoUsu() == null) {
                JOptionPane.showMessageDialog(null, "Datos de Usuario Incorrectos", "Validacion de Datos", JOptionPane.INFORMATION_MESSAGE);
                return;
            }

            this.dispose();
        } catch (Exception ex) {
            ConnectionTool.rollBackTrans(con);
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Configuraci칩n", JOptionPane.ERROR_MESSAGE);
        } finally {
            ConnectionTool.killConnection(con);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

